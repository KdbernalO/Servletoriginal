    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        processRequest(request, response);
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        //processRequest(request, response);
        String s1 = request.getParameter("valor1");
        String s2 = request.getParameter("valor2");
        String s3 = request.getParameter("valor3");
        String s4 = request.getParameter("valor4");
        int i = Integer.parseInt(s1);
        int j = Integer.parseInt(s2);
        int k = Integer.parseInt(s3);
        int l = Integer.parseInt(s4);
        int res = (i*l)-(j*k);
        
        PrintWriter salida = response.getWriter(); //canal de salida que transfiere informacion del servlet al usuario
        String cambio = "<span style='background-color:yellow;color:red;font-family:couriere'>";
        salida.println(cambio + "El determinante es: " + res + "</span>");
        
    }

